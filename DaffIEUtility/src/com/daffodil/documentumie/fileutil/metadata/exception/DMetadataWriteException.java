package com.daffodil.documentumie.fileutil.metadata.exception;


public class DMetadataWriteException extends Exception {

    public DMetadataWriteException () {
    	super();
    }

    public DMetadataWriteException (String arg0, Throwable arg1) {
    	super(arg0, arg1);
    }

    public DMetadataWriteException (String arg0) {
    	super(arg0);
    }

    public DMetadataWriteException (Throwable arg1) {
    	super(arg1);
    }

}

