package com.daffodil.documentumie.fileutil.configurator.exception;

public class DConfigReadException extends Exception {

	public DConfigReadException() {
		super();
	}

	public DConfigReadException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DConfigReadException(String arg0) {
		super(arg0);
	}

	public DConfigReadException(Throwable arg0) {
		super(arg0);
	}

}