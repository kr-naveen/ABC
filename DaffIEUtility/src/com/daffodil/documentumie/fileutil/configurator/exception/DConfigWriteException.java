package com.daffodil.documentumie.fileutil.configurator.exception;

public class DConfigWriteException extends Exception {

	public DConfigWriteException() {
		super();
	}

	public DConfigWriteException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DConfigWriteException(String arg0) {
		super(arg0);
	}

	public DConfigWriteException(Throwable arg0) {
		super(arg0);
	}
}